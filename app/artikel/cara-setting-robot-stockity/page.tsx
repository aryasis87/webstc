function calcSteps(nominal: number, multiplier: number, steps: number) {
  const arr: number[] = [];
  let n = nominal;
  for (let i = 0; i < steps; i++) {
    if (i === 0) {
      arr.push(Math.round(nominal * multiplier));
    } else {
      arr.push(Math.round(arr[i - 1] * multiplier));
    }
    n = arr[arr.length - 1];
  }
  return arr;
}

